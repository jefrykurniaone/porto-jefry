/**
 * gen-build-meta.mjs
 *
 * Build-time codegen script: derives LAST_MODIFIED_DATE from the last git commit
 * and writes it into src/utils/constants.ts.
 *
 * Run: node scripts/gen-build-meta.mjs
 * Wired via: npm prebuild → runs automatically before `next build`.
 *
 * Security note (T-04-03): git is invoked via execFileSync with a fixed argument
 * array — no string interpolation, no `shell: true`, no user/runtime input.
 */

import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = join(__dirname, '..');
const CONSTANTS_PATH = join(REPO_ROOT, 'src', 'utils', 'constants.ts');
const DATE_REGEX = /^\d{4}-\d{2}-\d{2}$/;

/**
 * Derive the last git commit date (YYYY-MM-DD) via `git log -1 --format=%cs`.
 * Falls back to today's date if git is unavailable or returns an unexpected value
 * (T-04-04: safe fallback so the build never breaks).
 *
 * @returns {string} Date string in YYYY-MM-DD format
 */
function getLastCommitDate() {
    try {
        const raw = execFileSync('git', ['log', '-1', '--format=%cs'], {
            cwd: REPO_ROOT,
            encoding: 'utf8',
        }).trim();

        if (DATE_REGEX.test(raw)) {
            return raw;
        }

        process.stderr.write(
            `[gen-build-meta] WARNING: git returned unexpected output "${raw}"; ` +
            'falling back to current date.\n'
        );
    } catch (err) {
        process.stderr.write(
            `[gen-build-meta] WARNING: git unavailable (${err.message}); ` +
            'falling back to current date.\n'
        );
    }

    return new Date().toISOString().slice(0, 10);
}

/**
 * Read the current constants.ts, replace only the LAST_MODIFIED_DATE line,
 * and write the file back. BASE_URL is preserved verbatim.
 *
 * @param {string} date - YYYY-MM-DD date string
 */
function updateConstants(date) {
    const existing = readFileSync(CONSTANTS_PATH, 'utf8');

    // Preserve the BASE_URL line (and any other non-LAST_MODIFIED_DATE content).
    const baseUrlLine = existing
        .split('\n')
        .find((line) => line.startsWith('export const BASE_URL'));

    if (!baseUrlLine) {
        throw new Error(
            `[gen-build-meta] Could not find BASE_URL export in ${CONSTANTS_PATH}`
        );
    }

    const generated =
        `${baseUrlLine}\n` +
        `\n` +
        `// Generated by scripts/gen-build-meta.mjs — do not edit manually\n` +
        `export const LAST_MODIFIED_DATE = '${date}';\n`;

    writeFileSync(CONSTANTS_PATH, generated, 'utf8');
    process.stdout.write(
        `[gen-build-meta] LAST_MODIFIED_DATE set to ${date} in src/utils/constants.ts\n`
    );
}

const commitDate = getLastCommitDate();
updateConstants(commitDate);
